import mongoose, { Document, Schema } from "mongoose";
import { IUser } from "../types";
const usersSchema: Schema<IUser> = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const User = mongoose.model<IUser>("User", usersSchema);
export default User;
